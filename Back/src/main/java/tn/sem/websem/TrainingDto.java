package tn.sem.websem;

public class TrainingDto {

    private String titleTraining ;
    private String durationTraining ;
    private String descriptionTraining ;

    public String getTitleTraining() {
        return titleTraining;
    }

    public void setTitleTraining(String titleTraining) {
        this.titleTraining = titleTraining;
    }

    public String getDurationTraining() {
        return durationTraining;
    }

    public void setDurationTraining(String durationTraining) {
        this.durationTraining = durationTraining;
    }

    public String getDescriptionTraining() {
        return descriptionTraining;
    }

    public void setDescriptionTraining(String descriptionTraining) {
        this.descriptionTraining = descriptionTraining;
    }
}
